package com.javaex.practice;

public class Ex01 {

	public static void main(String[] args) {

		System.out.println("while문");
		int i = 0;
		while(i<3) {
			System.out.println("i=" + i);
			i++;
		}
		System.out.println("for문");
		for(int o=0; o<3; o++) {
			System.out.println("o=" + o);
		}
	}
}
